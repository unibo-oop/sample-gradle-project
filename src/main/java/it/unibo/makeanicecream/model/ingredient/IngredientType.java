package it.unibo.makeanicecream.model.ingredient;

/**
 * IngredientType is an enumeration that defines the different types of ingredients that can be used in an ice cream.
 */
public enum IngredientType {
    /**
     * SCOOP represents a flavor scoop ingredient.
     * LIQUID_TOPPING represents a liquid topping ingredient.
     * SOLID_TOPPING represents a solid topping ingredient.
     */
    SCOOP, 
    LIQUID_TOPPING,
    SOLID_TOPPING,
}
